    $(window).scroll(function () {
      var scrollTop = $(window).scrollTop();
      const bannerHeight = $('.fix_top').height() + $('.kv').height() + $('.gift').height() + $('.brand').height();
      const contentHeight = $('.content').height();
      const bgHeight = $('.bg').height();
      if (scrollTop >= bannerHeight && scrollTop < (contentHeight +
          bannerHeight - bgHeight)) { //超过banner，未超过content-bannerHeight 
        $('.bg').css({
          'position': 'sticky',
          'top': $('.fix_top').height()
        })
        $(".content").css('margin-top', '-100vh')
      } else if (scrollTop >= (contentHeight +
